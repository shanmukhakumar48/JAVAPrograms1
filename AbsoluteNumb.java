
public class AbsoluteNumb {  //positive nums are absolute numbers 
	                                // 

	public static void main(String[] args) {

		double i=-78.799; 
		int   j=-4;                            //Converting negative nums to positive nums using math.abs
		long l=-88888888L;
		System.out.println(Math.abs(i));
		System.out.println(Math.abs(j));
		System.out.println(Math.abs(l));
	}

}
